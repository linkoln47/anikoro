CREATE TABLE IF NOT EXISTS anime_franchise_members (
    anime_id INTEGER PRIMARY KEY REFERENCES anime_catalog(id) ON DELETE CASCADE,
    franchise_id INTEGER NOT NULL REFERENCES anime_franchises(id) ON DELETE CASCADE,
    CHECK (anime_id > 0)
);

DO $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM information_schema.table_constraints
        WHERE table_name = 'anime_franchise_members'
          AND constraint_name = 'anime_franchise_members_pkey'
          AND constraint_type = 'PRIMARY KEY'
    ) AND EXISTS (
        SELECT 1
        FROM information_schema.key_column_usage
        WHERE table_name = 'anime_franchise_members'
          AND constraint_name = 'anime_franchise_members_pkey'
          AND column_name = 'franchise_id'
    ) THEN
        ALTER TABLE anime_franchise_members
            DROP CONSTRAINT anime_franchise_members_pkey;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM information_schema.table_constraints
        WHERE table_name = 'anime_franchise_members'
          AND constraint_name = 'anime_franchise_members_pkey'
          AND constraint_type = 'PRIMARY KEY'
    ) THEN
        ALTER TABLE anime_franchise_members
            ADD PRIMARY KEY (anime_id);
    END IF;
END $$;

UPDATE anime_franchises f
SET member_key = COALESCE(
    (
        SELECT string_agg(fm.anime_id::TEXT, ':' ORDER BY fm.anime_id)
        FROM anime_franchise_members fm
        WHERE fm.franchise_id = f.id
    ),
    f.id::TEXT
)
WHERE member_key IS NULL;

ALTER TABLE anime_franchises
    ALTER COLUMN member_key SET NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS anime_franchises_member_key_idx
    ON anime_franchises (member_key);

CREATE INDEX IF NOT EXISTS anime_franchise_members_franchise_idx
    ON anime_franchise_members (franchise_id, anime_id);

CREATE TABLE IF NOT EXISTS anime_list_statuses (
    id SMALLINT PRIMARY KEY,
    code TEXT NOT NULL,
    CHECK (id > 0),
    CHECK (code <> '')
);

CREATE UNIQUE INDEX IF NOT EXISTS anime_list_statuses_code_idx
    ON anime_list_statuses (code);

